/**
 * Extracts string literals from toast({ ... }) blocks and writes UTF-8 phrase maps.
 * Run: node scripts/build-toast-phrase-map.mjs
 */
import fs from "node:fs";
import path from "node:path";
import crypto from "node:crypto";

const appRoot = path.join(process.cwd(), "app");
const outDir = path.join(process.cwd(), "app", "lib", "i18n", "generated");

function extractFromToastBlocks(src) {
  const found = new Set();
  let searchFrom = 0;
  while (true) {
    const t = src.indexOf("toast({", searchFrom);
    if (t === -1) break;
    const open = src.indexOf("{", t + 5);
    if (open === -1) break;
    let depth = 0;
    let i = open;
    for (; i < src.length; i++) {
      const c = src[i];
      if (c === "{") depth++;
      else if (c === "}") {
        depth--;
        if (depth === 0) {
          const block = src.slice(open, i + 1);
          const re = /\b(?:title|description):\s*"((?:\\.|[^"\\])*)"/g;
          let m;
          while ((m = re.exec(block))) {
            try {
              found.add(JSON.parse(`"${m[1]}"`));
            } catch {
              found.add(m[1]);
            }
          }
          searchFrom = i + 1;
          break;
        }
      }
    }
    if (i >= src.length) break;
  }
  return found;
}

function walk(dir, acc) {
  for (const name of fs.readdirSync(dir, { withFileTypes: true })) {
    const p = path.join(dir, name.name);
    if (name.isDirectory()) walk(p, acc);
    else if (name.name.endsWith(".tsx") || name.name.endsWith(".ts")) {
      const src = fs.readFileSync(p, "utf8");
      if (!src.includes("toast({")) continue;
      for (const s of extractFromToastBlocks(src)) acc.add(s);
    }
  }
}

function stableId(s) {
  return crypto.createHash("sha256").update(s, "utf8").digest("hex").slice(0, 12);
}

function main() {
  const acc = new Set();
  walk(appRoot, acc);
  const sorted = [...acc].sort((a, b) => a.localeCompare(b));

  const enToPath = {};
  const pathToEn = {};
  for (const s of sorted) {
    const id = stableId(s);
    const p = `toast.phrases.${id}`;
    enToPath[s] = p;
    pathToEn[p] = s;
  }

  fs.mkdirSync(outDir, { recursive: true });

  const ts = `/* eslint-disable */
// Auto-generated by scripts/build-toast-phrase-map.mjs — do not edit by hand.
export const toastEnglishToPath: Record<string, string> = ${JSON.stringify(enToPath, null, 2)};

export const toastPathToEnglish: Record<string, string> = ${JSON.stringify(pathToEn, null, 2)};

export const toastPhrasePaths: readonly string[] = ${JSON.stringify(Object.keys(pathToEn).sort(), null, 2)};
`;

  fs.writeFileSync(path.join(outDir, "toastPhraseMap.ts"), ts, "utf8");
  fs.writeFileSync(
    path.join(outDir, "toastPathToEnglish.json"),
    JSON.stringify(pathToEn, null, 2),
    "utf8",
  );
  console.log(`Wrote ${sorted.length} toast phrases to generated/toastPhraseMap.ts`);
}

main();
